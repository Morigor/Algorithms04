public class BiggerValue {

    //  Напишите алгоритм BiggerValue, который из двух параметров типа int
    //  возвращает бОльшее значение.
    public int biggerValueAlgo(int num1, int num2) {
        if(num1 >= num2) {
            return num1;
        } else {
            return num2;
        }
    }

}
